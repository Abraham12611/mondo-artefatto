export function getPieceBySlug(slug: string): Piece | undefined {
  return pieces.find((p) => p.slug === slug);
}

export function getAdjacentPieces(slug: string): {
  prev: Piece;
  next: Piece;
} {
  const index = pieces.findIndex((p) => p.slug === slug);
  const prev = pieces[(index - 1 + pieces.length) % pieces.length];
  const next = pieces[(index + 1) % pieces.length];
  return { prev, next };
}
